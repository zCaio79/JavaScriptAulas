
// IF, ELSE IF, ELSE

const idade = 15;

if (idade == 18) {
    console.log("Você completou 18 anos!!");
}
else if (idade < 18) {
    console.log("Você é menor de idade!!");
}
else {
    console.log("Você é maior de idade!!");
}

// SWITCH CASE

const numero = 2;

switch (numero) {
    case 1:
        console.log("Número 1");
        break;
    case 2:
        console.log("Número 2");
        break;
    case 3:
        console.log("Número 3");
        break;
    default:
        console.log("Outro Número");
        break;
}

